    private String buildRetrievalLabel(String retrievalMode) {
        if ("TEXT_ONLY".equalsIgnoreCase(retrievalMode)) {
            return "关键词召回";
        }
        return "混合召回（语义相关 + 关键词命中）";
    }

    private String buildEvidenceSnippet(String userMessage, String anchorText, String matchedChunkText) {
        String normalizedAnchorText = normalizeEvidenceText(anchorText);
        if (!normalizedAnchorText.isBlank()) {
            return trimToMaxLength(normalizedAnchorText, MAX_EVIDENCE_SNIPPET_LEN);
        }

        String normalizedMatchedChunk = normalizeEvidenceText(matchedChunkText);
        if (normalizedMatchedChunk.isBlank()) {
            String normalizedUserMessage = normalizeEvidenceText(userMessage);
            if (!normalizedUserMessage.isBlank()) {
                return trimToMaxLength(normalizedUserMessage, MAX_EVIDENCE_SNIPPET_LEN);
            }
            return "";
        }

        String[] sentences = normalizedMatchedChunk.split("(?<=[。！？!?；;])");
        for (String sentence : sentences) {
            String trimmedSentence = sentence.trim();
            if (trimmedSentence.length() >= 12) {
                return trimToMaxLength(trimmedSentence, MAX_EVIDENCE_SNIPPET_LEN);
            }
        }

        return trimToMaxLength(normalizedMatchedChunk, MAX_EVIDENCE_SNIPPET_LEN);
    }

    private String trimToMaxLength(String value, int maxLength) {
        String normalized = normalizeEvidenceText(value);
        if (normalized.length() <= maxLength) {
            return normalized;
        }
        return normalized.substring(0, maxLength) + "…";
    }

    private String normalizeEvidenceText(String value) {
        return normalizeEvidenceTextStatic(value);
    }

    private static String normalizeEvidenceTextStatic(String value) {
        return value == null ? "" : value.replaceAll("\\s+", " ").trim();
    }

    private static final class ChatRequestTiming {
        private final long startedAtNanos = System.nanoTime();
        private final int userQueryLength;
        private final Map<String, Long> stageMs = new LinkedHashMap<>();
        private final Map<String, Long> activeStageStarts = new HashMap<>();
        private String route = "UNKNOWN";
        private int retrievalHitCount;
        private int citationCount;
        private long lastMarkNanos = startedAtNanos;

        private ChatRequestTiming(String userMessage) {
            this.userQueryLength = userMessage == null ? 0 : userMessage.length();
        }

        static ChatRequestTiming start(String userMessage) {
            return new ChatRequestTiming(userMessage);
        }

        void route(String route) {
            this.route = route;
        }

        void mark(String stage) {
            long now = System.nanoTime();
            stageMs.merge(stage, TimeUnit.NANOSECONDS.toMillis(now - lastMarkNanos), Long::sum);
            lastMarkNanos = now;
        }

        void begin(String stage) {
            activeStageStarts.put(stage, System.nanoTime());
        }

        void end(String stage) {
            Long stageStartedAt = activeStageStarts.remove(stage);
            if (stageStartedAt != null) {
                stageMs.merge(stage, TimeUnit.NANOSECONDS.toMillis(System.nanoTime() - stageStartedAt), Long::sum);
            }
        }

        void retrievalHitCount(int retrievalHitCount) {
            this.retrievalHitCount = Math.max(this.retrievalHitCount, retrievalHitCount);
        }

        void citationCount(int citationCount) {
            this.citationCount = citationCount;
        }

        void log(Logger logger, String conversationId, String generationId) {
            logger.info(
                    "chat timing: conversationId={}, generationId={}, route={}, userQueryLength={}, retrievalHitCount={}, citationCount={}, totalMs={}, stageMs={}",
                    conversationId,
                    generationId,
                    route,
                    userQueryLength,
                    retrievalHitCount,
                    citationCount,
                    TimeUnit.NANOSECONDS.toMillis(System.nanoTime() - startedAtNanos),
                    stageMs
            );
        }
    }

